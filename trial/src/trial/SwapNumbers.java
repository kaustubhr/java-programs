package trial;


public class SwapNumbers {
	
	public static void main(String[] args) {
		int first=20;
		int second=30;
		System.out.println("First number = "+first+" Second number= "+second);
		int result;
		result=first;
		first=second;
		second=result;
		System.out.println("First number = "+first+" Second number= "+second);
		
	}
}
